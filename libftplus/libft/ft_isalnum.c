#include "libft.h"

int	ft_isalnum(int sub)
{
	if ((sub > 47 && sub < 58) || (sub > 64 && sub < 91))
		return (1);
	else if (sub > 96 && sub < 123)
		return (1);
	else
		return (0);
}
