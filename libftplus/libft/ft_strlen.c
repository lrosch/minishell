#include "libft.h"

size_t	ft_strlen(const char *src)
{
	size_t	x;

	x = 0;
	if (!src)
		return (0);
	if (src[x])
	{
		while (src[x] != 0)
		{
			x++;
		}
	}
	return (x);
}
